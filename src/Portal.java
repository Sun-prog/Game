public class Portal {
    int speed;
    int x;
    int y;

    public Portal(int speed, int x, int y) {
        this.speed = speed;
        this.x = x;
        this.y = y;
    }

    public int getSpeed() {
        return speed;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
